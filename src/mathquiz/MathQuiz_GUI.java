    private void btnSavePreActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSavePreActionPerformed
        
        if (btquestionList.size() > 0)
        {
             btquestionList.reSetTraversalString();
            btquestionList.preOrder(btquestionList.getRoot());
            String output = btquestionList.getTraversalString();
            FileOutputStream out = null;

          try {
             out = new FileOutputStream("preOrder.txt");
             byte [] outputBytes = output.getBytes();
             out.write(outputBytes);
          }
          catch (FileNotFoundException fnfe)
          {
              JOptionPane.showMessageDialog(null, "File not found!", "FILE NOT FOUND ERROR", JOptionPane.ERROR_MESSAGE);
          }
          catch (IOException ioe)
          {
               JOptionPane.showMessageDialog(null, "File not writable", "FILE NOT WRITABLE ERROR", JOptionPane.ERROR_MESSAGE);
          }
          catch (Exception e)
          {
               JOptionPane.showMessageDialog(null, "File IO Problem!", "FILE IO ERROR", JOptionPane.ERROR_MESSAGE);
          }
        }
       

    }//GEN-LAST:event_btnSavePreActionPerformed

