    private static void displayDataOnTable(List<Question> alquestionList) {
        
        Iterator i = alquestionList.iterator();
        
        for(int j = questiontable.getRowCount() - 1; j >= 0; j--){
            questiontable.removeRow(j);
        }
        
        while(i.hasNext()){
            questiontable.addRow(i.next().toString().split(" "));
        }
    }
    
